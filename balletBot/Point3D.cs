namespace balletBot
{
    /// <summary>
    /// The Point3D class allows for the use of 3-dimensional space in computations
    /// in other classes.
    /// </summary>
    class Point3D
    {
        public float x;
        public float y;
        public float z;

        public Point3D(float x, float y, float z)
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}
